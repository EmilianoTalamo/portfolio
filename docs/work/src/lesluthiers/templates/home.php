<section id="about">
	<h2>Acerca del grupo</h2>
	<p>El conjunto Les Luthiers es un grupo de músicos-actores-humoristas muy popular en la Argentina, España y Latinoamérica. Se conocieron hace más de 50 años en uno de los coros de la Universidad de Buenos Aires y su nombre proviene de la palabra francesa luthier, que es la que también se usa en castellano para designar al artesano que fabrica o repara instrumentos musicales. Se bautizaron Les Luthiers porque una de las características de sus actuaciones es el uso de insólitos instrumentos fabricados por ellos mismos.</p>
	<p>Sus espectáculos combinan parodias de géneros musicales clásicos y populares con escenas teatrales de gran eficacia humorística. Todas las letras, músicas y arreglos son creación de los integrantes históricos del conjunto: Carlos López Puccio, Jorge Maronna, Marcos Mundstock, Carlos Núñez Cortés y Daniel Rabinovich.</p>
	<div>

		<div><h3>1960</h3></div>
		<div>
			<p>En septiembre de 1965, al finalizar el Festival de Coros Universitarios realizado en la ciudad de Tucumán, integrantes del Coro de la Facultad de Ingeniería de Buenos Aires presentaron para los demás participantes un espectáculo de música y humor.</p>
			<p>Se trataba de la parodia de un concierto formal, con solistas y coro acompañados por una orquesta de instrumentos no convencionales, que los mismos intérpretes habían construido con materiales caseros.</p>
			<p>La obra central de aquel programa era la Cantata Modatón (luego Laxatón), compuesta por uno de los integrantes del grupo, el arquitecto Gerardo Masana, quien también fue el inventor de la mayoría de los instrumentos utilizados en aquella oportunidad. La música parodiaba una cantata barroca, con un texto tomado del prospecto de un conocido laxante.</p>
			<p>Al poco tiempo, el grupo fue contratado para repetir el espectáculo en una sala de la capital argentina, donde el se presentó, con el nombre de I Musicisti, para ofrecer unas pocas representaciones. El éxito fue grande y las actuaciones debieron prolongarse por espacio de tres meses.</p>
			<p>Este triunfo resultó decisivo para que, poco después, el grupo fuera invitado a participar en el espectáculo "Mens sana in corpore sano" en el Instituto Di Tella, templo de las vanguardias artísticas de Buenos Aires.</p>
		</div>

		<div><h3>1970</h3></div>
		<div>
			<p>En 1970, Les Luthiers ingresó al circuito porteño de cafés concert, con Querida Condesa: Cartas de Johann Sebastian Mastropiero a la condesa de Shortshot.Aquel espectáculo, presentado en el café concert La Cebolla, significó la incorporación definitiva de Mastropiero al repertorio del grupo.</p>
			<p>El personaje había sido creado en 1962 por Marcos Mundstock, quien leía su biografía en fiestas del coro y otros eventos informales. Lo acompañaba con el piano Rodolfo Melín, otro integrante del coro, ya fallecido.</p>
			<p>Freddy Mastropiero fue su primer nombre. “Le puse Mastropiero porque sonaba medio mafioso”, explica Mundstock en Les Luthiers de la L a la S. “Y Freddy porque tenía un sabor gracioso antes del apellido italiano”.</p>
		</div>

		<div><h3>1980</h3></div>
		<div>
			<p>Durante los años 80, el conjunto realizó tres funciones inolvidables. La primera tuvo lugar en la sala Avery Fisher Hall del Lincoln Center de Nueva York, el 2 de noviembre de 1980, con un espectáculo traducido íntegramente al inglés.La respuesta, tanto del público como de la crítica, fue altamente favorable. El New York Times escribió que tenían “credenciales excepcionales”, y Variety señaló que el debut había probado ser “un éxito hilarante”.</p>
			<p>Por aquel entonces, los luthiers no tomaban muy en serio la posibilidad de tocar en el Teatro Colón, uno de los más importantes escenarios de la lírica mundial; aquella idea les resultaba lejana. 
			Sin embargo, el 1 de agosto de 1986, el público porteño se enteró, a través de una rueda de prensa difundida masivamente, de que Les Luthiers se presentaría, ese mes, en el mencionado teatro, donde interpretaría una recopilación de obras llamada Recital Sinfónico 86, a beneficio de la fundación "Convivir", de lucha contra la drogadicción.</p>
			<p>Las entradas se pusieron a la venta tres días antes, y se agotaron en un par de horas. Tan grande fue la repercusión que se decidió poner a la venta localidades para presenciar el ensayo general, que se realizaría el mismo día de la función, a las 11 de la mañana. Nuevamente, las entradas se agotaron en unas horas.</p>
			<p>Finalmente, el 11 de agosto los luthiers se subieron a las tablas del Colón. Allí interpretaron cinco obras solos, y luego otras tres con la orquesta sinfónica del teatro: el Concerto Grosso alla rustica, el Concierto de Mpkstroff y la zarzuela náutica Las majas del bergantín.</p>
			<p>Entre el ensayo y la función, Les Luthiers convocó a 6.000 espectadores.</p>
		</div>

		<div><h3>1990</h3></div>
		<div>
			<p>La década comenzó con una buena noticia: la publicación de un libro sobre la historia del conjunto. Se trata de Les Luthiers de la L a la S, del reconocido periodista y escritor colombiano Daniel Samper Pizano (Ediciones de La Flor, 1991).</p>
			<p>Gracias a este libro, pleno de fotografías, documentos y anécdotas, los fans pudieron conocer la génesis y los detalles de la trayectoria del conjunto.</p>
			<p>Les Luthiers estrenó en los 90 su segundo espectáculo de recopilación:Grandes Hitos (1992). Le siguieron Unen canto con humor (1994), Bromato de armonio (1996) y Todo por que rías (1999).</p>
			<p>En 1994, un problema cardíaco de Daniel Rabinovich durante una gira por España, motivó su momentáneo alejamiento del escenario. Tras barajar distintas opciones, se optó por contratar al músico y cantante Horacio Fontova como reemplazante durante algunas funciones.</p>
			<p>En 1995, Les Luthiers sufrió una importante pérdida con el fallecimiento de Carlos Iraldi, quien durante los veinte años anteriores había sido el principal constructor de instrumentos de Les Luthiers.</p>
		</div>

		<div><h3>2000</h3></div>
		<div>
			<p>Los Luthiers iniciaron el nuevo siglo con una novedad: la incorporación de “suplentes”, que eventualmente pudieran reemplazar a cualquiera de los integrantes en caso de necesidad.</p>
			<p>La selección de los candidatos no fue nada sencilla. Los postulantes tenían que saber cantar, moverse sobre el escenario e interpretar distintos instrumentos. Finalmente, se eligió a Gustavo López Manzitti (quien se retiraría tres años más tarde) y a Horacio “Tato” Turano, cantante, pianista, saxofonista y compositor.</p>
			<p>Desde mediados de 2000, Turano viaja con Les Luthiers a todos los lugares en los que el grupo se presenta.</p>
		</div>

	</div>
</section>

